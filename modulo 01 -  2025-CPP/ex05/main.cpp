#include "Harl.hpp"


int main()
{
	Harl bazinga;

	std::cout << "// ------------------------------------------------------------------------------//" << std::endl;
	std::cout << "//                                   DEBUG                                      //" << std::endl;
	std::cout << "// ------------------------------------------------------------------------------//" << std::endl;
	bazinga.complain("DEBUG");
	std::cout << std::endl;

	std::cout << "// ------------------------------------------------------------------------------//" << std::endl;
	std::cout << "//                                   INFO                                       //" << std::endl;
	std::cout << "// ------------------------------------------------------------------------------//" << std::endl;
	bazinga.complain("INFO");
	std::cout << std::endl;
	
	std::cout << "// ------------------------------------------------------------------------------//" << std::endl;
	std::cout << "//                                   WARNING                                    //" << std::endl;
	std::cout << "// ------------------------------------------------------------------------------//" << std::endl;
	bazinga.complain("WARNING");
	std::cout << std::endl;

	std::cout << "// ------------------------------------------------------------------------------//" << std::endl;
	std::cout << "//                                    ERROR                                     //" << std::endl;
	std::cout << "// ------------------------------------------------------------------------------//" << std::endl;
	bazinga.complain("ERROR");
	std::cout << std::endl;

	std::cout << "// ------------------------------------------------------------------------------//" << std::endl;
	std::cout << "//                                   BAZINGA                                    //" << std::endl; 
	std::cout << "// ------------------------------------------------------------------------------//" << std::endl;
	bazinga.complain("BAZINGA"); 
	std::cout << std::endl;

	return (0);
}
